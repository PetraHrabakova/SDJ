// Yo what's up
// public - access specifier
// class - beginning of a class definition
public class Simple
{
  // main - name of method
  public static void main(String[] args)    // method header - marks the beginning of a method
  { // variable = named storage location in the computer's memory
    // literal = value that is written into the code of a program
    // identifier = name defined by a programmer that represents some element of the program
    int value;
    int value_1;

    // value = variable
    // 5 = integer literal (if it was a word, it would be String literal)
    value = 5;
    value_1 = 20;

    // System - class
    // out - object
    // println - method
    System.out.print("\bThe value is \n");
    System.out.println(value);
    System.out.println(value_1);
  }
}
